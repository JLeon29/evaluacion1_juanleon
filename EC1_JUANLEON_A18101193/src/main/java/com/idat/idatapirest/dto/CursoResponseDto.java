package com.idat.idatapirest.dto;

public class CursoResponseDto {
	
	private Integer idResponse;
	private String cursoCurso;
	private String descripcionCurso;
	
	public Integer getIdResponse() {
		return idResponse;
	}
	public void setIdResponse(Integer idResponse) {
		this.idResponse = idResponse;
	}
	public String getCursoCurso() {
		return cursoCurso;
	}
	public void setCursoCurso(String cursoCurso) {
		this.cursoCurso = cursoCurso;
	}
	public String getDescripcionCurso() {
		return descripcionCurso;
	}
	public void setDescripcionCurso(String descripcionCurso) {
		this.descripcionCurso = descripcionCurso;
	}
	
	
	

}
